package it.bitrock.academy;

public class VariablesAndPrimitiveTypes {

    public static void main(String[] args) {
        byte aByte = 12;
        short aShort = 23;
        int anInt = 34;
        long aLong = 45;
        float aFloat = 5.6f;
        double aDouble = 6.7;
        boolean aBoolean = true;
        char aChar = 'c';

        System.out.println("this is a byte " + aByte);
        System.out.println("this is a short " + aShort);
        System.out.println("this is an int " + anInt);
        System.out.println("this is a long " + aLong);
        System.out.println("this is a float " + aFloat);
        System.out.println("this is a double " + aDouble);
        System.out.println("this is a boolean " + aBoolean);
        System.out.println("this is a char " + aChar);
    }

}
